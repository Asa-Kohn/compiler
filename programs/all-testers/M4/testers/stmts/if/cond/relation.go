package main

func main() {

	if 0 == 0 {
		print(0)
	}

	if 0 != 1 {
		print(0)
	}

	if 0 < 1 {
		print(0)
	}

	if 1 > 0 {
		print(0)
	}

	if 0 >= 0 {
		print(0)
	}

	if 0 <= 0 {
		print(0)
	}

	if !(0 == 0) {
		print(0)
	}

	if !(0 != 1) {
		print(0)
	}

	if !(0 > 1) {
		print(0)
	}

	if !(1 < 0) {
		print(0)
	}

	if !(0 >= 0) {
		print(0)
	}

	if !(0 <= 0) {
		print(0)
	} else {
		print(1)
	}

}
