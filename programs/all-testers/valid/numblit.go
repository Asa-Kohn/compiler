//print numerical lits
package main

func main() {
	//INT LITS
	//decimal
	print(255);
	print(" : ");
	println(255);
	//octal
	print(0377);
	print(" : ");
	println(0377);
	//hex
	print(0xff);
	print(" : ");
	println(0xff);

	//FLOAT LITS
	//int+dec
	print(0.12);
	print(" : ");
	println(0.12);
	//dec only
	print(.12);
	print(" : ");
	println(.12);
	//int only
	print(12.);
	print(" : ");
	println(12.);
}
