//print strings with esc chars
package main

func main() {
	println("alert: \a");
	println("backspace: \b");
	println("form feed: \f");
	println("line feed or newline: \n");
	println("carriage return: \r");
	println("horizontal tab: \t");
	println("vertical tab: \v");
	println("backslash: \\");
	println("double quote(legal only in string literals): \"");
	println("single quote: ' ");
}
