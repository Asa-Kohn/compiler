//print strings with esc chars 
package main

import "fmt"

func main() {
	fmt.Println("alert: \a");
	fmt.Println("backspace: \b");
	fmt.Println("form feed: \f");
	fmt.Println("line feed or newline: \n");
	fmt.Println("carriage return: \r");
	fmt.Println("horizontal tab: \t");
	fmt.Println("vertical tab: \v");
	fmt.Println("backslash: \\");
	fmt.Println("double quote(legal only in string literals): \"");
	fmt.Println("single quote: ' ");
}